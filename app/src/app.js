window.ethereum.on("accountsChanged", () => {
  GetAccountAddress()
});

window.ethereum.on("chainChanged", () => {
  GetAccountAddress()
});

function connectMetaMask(){
  if (typeof web3 !== "undefined") {
    // MetaMask is install
    web3 = new Web3(Web3.givenProvider);
  }
  else {
    console.log("Need to install MetaMask")
  }
  ethereum.request({ method: "eth_requestAccounts" });
  GetAccountAddress()
}

async function GetAccountAddress(){
  // get account address
  AccountAddress = (await web3.eth.getAccounts())[0];
  document.getElementById('AAddress').innerHTML = AccountAddress
  // init SC
  if (!(AccountAddress in Contracts)){
    Contracts[AccountAddress] = new web3.eth.Contract(abi,AccountAddress,{})
  }
  Contract = Contracts[AccountAddress]
}

function EventListener(){
  // connect button
  const ConnectButton = document.getElementById('connect');
  ConnectButton.addEventListener('click', function(e) {
    connectMetaMask()
  });
  // Add skin button
  const AddSkinButton = document.getElementById('add')
  AddSkinButton.addEventListener('click', function(e){
	value = document.getElementById('name').value
	document.getElementById('name').value = null
    Contract.methods
	.addSkin("value")
	.send({from: AccountAddress})
	.on("receipt", (receipt) => {
        alert("Success")
      })
      .on("error", (err) => {
        alert("Error")
      });
  });
}